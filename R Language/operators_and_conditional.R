# OPERATORS AND CONDITIONAL STATEMENTS IN R

# logical operators:
# logical operators return a logical data type such as TRUE or FALSE. 
# there are three primary types of logical operators:
# 1.AND (sometimes represented as & or && in R), 2. OR (sometimes represented as | or || in R) and 3. NOT (!).
print(3>2 & 2 < 4)
print('a' > 'b' | 'c' < 'd')


